package com.shyamu53r.mathoperations;

import android.content.Context;
import android.widget.TextView;

public class MathOps {
    public static int add(Context c, int a, int b){
        return a+b;
    }
    public static int sub(Context c, int a, int b){
        return a-b;
    }
    public static int multiply(Context c, int a, int b){
        return a*b;
    }
    public static int divide(Context c, int a, int b){
        return a/b;
    }
}
